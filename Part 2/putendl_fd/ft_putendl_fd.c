#include <unistd.h>

void	fT_putendl_fd(char const *s, int fd)
{
	int i;
	int c;

	i = 0;
	c = '\n';
	while (s[i] != '\0')
		write(fd, &s[i++], 1);
	write(fd, &c, 1);
}
