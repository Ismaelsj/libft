char	*ft_strrchr(const char* s, int c)
{
	char *str;
	int i;

	i = 0;
	str = (char*)s;
	while (*str != '\0')
	{
		str++;
		i++;
	}
	while (i >= 0)
	{
		if (*str == (char)c)
			return (str);
		str--;
		i--;
	}
	return (0);
}
